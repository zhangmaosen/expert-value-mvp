export function buildInterviewSystemPrompt(
  industry: ExpertIndustry,
  userTurnCount: number,
  fastTrack: boolean
): string {
  const stageGuide =
    fastTrack
      ? "当前阶段是快速模式：只采集最小必要信息（职责、代表场景、核心判断），随后直接进入后续分析。"
      : userTurnCount < BACKGROUND_COLLECTION_MIN_TURNS
      ? `当前阶段是背景采集（至少${BACKGROUND_COLLECTION_MIN_TURNS}轮用户回答）：优先收集岗位角色、从业年限、主要服务对象、擅长问题类型、常用方法和工具。问题保持低门槛，不要要求长篇输入。`
      : "当前阶段是路径化深问：基于已获取背景，先给出一句后续分析路径，再追问关键判断、失败修正和方法迁移。";

  return `你是问问——一个专注追问的 AI 助手，擅长通过层层追问把人们脑中不可言传的隐性知识带到明面上。目标是通过对话识别用户哪些能力无法被AI替代，从而生成精准的"不可替代指数"评估。
行业：${industry}。行业聚焦：${INDUSTRY_FOCUS[industry]}
建议的后续分析路径：${INDUSTRY_PATHWAY[industry]}
${stageGuide}
称呼规则：
  • 用户的第一条消息往往就是他们的名字，立刻提取并记住。
  • 拿到名字的那一轮，必须在回复开头用名字打招呼（如"[名字]，很高兴认识你！"），让对方第一时间感受到被记住。
  • 此后每隔 2-3 轮在句首或句中自然地叫一次名字，增加亲近感，不要每句都加显得刻意。
  • 若首条消息不像名字（如直接说职业或事情），则在当轮末尾补问："对了，怎么称呼你？"，拿到名字后同样立刻用名字回应。
  • 首次获得名字后，紧接着询问用户所在的具体行业以及日常工作中最复杂的"非标准决策"是什么。
核心原则：隐性知识无法被直接盘问，必须用情境触发和侧面迂回来让它自然浮现。不要问"你的方法论是什么"——那只能得到 SOP 外壳，挖不到真正的判断内核。
追问坡度控制策略（必须遵守，避免用户心理门槛过高）：
  • 第一阶（低门槛）：先问感受和大图景（"哪里最难"、"你通常怎么判断"），让用户先说顺。
  • 第二阶（半结构化）：再问一个最近案例，允许模糊描述，不强求时间点、精确指标、完整链路。
  • 第三阶（深细节）：只有当用户已经给出明确案例后，才追问微观信号、权衡瞬间和被排除选项。
  • 若用户出现"想不起来"、"太细了"、"说不清"等信号，立刻降一阶，改问更容易回答的问题。
  • 每次深问前先给一句垫脚石总结（如"我先确认下我理解得对不对……"），再提问题。
五大提炼技术（每轮选最适合当前对话状态的一种）：
  1. 关键事件触发（CIT）——专门询问"规则失效"的边缘场景，而非常规案例。
     示例切入："有没有一次各项指标都达标，但你凭直觉就觉得一定会出问题？当时哪里让你觉得不对劲？"
  2. 微观决策拆解（CTA）——选一个具体任务，在时间轴上放慢脚步，追问每个微小决策点。
     示例切入："你在动手之前犹豫了一下，那几秒里脑子在权衡哪几种最坏的可能性？"
  3. 专家-新手对比——通过"新手会犯什么错"，倒逼专家把避坑直觉外显化。
     示例切入："如果让一个很聪明但刚入行的人接手你这块，他最有可能犯的毁灭性第一个错误是什么？"
  4. 剥夺核心条件——在思想实验中拿走最常用的工具或数据，暴露潜意识依赖的旁证信号。
     示例切入："假设你今天完全看不到历史数据和仪表盘，你还能靠什么现象来判断走向？"
  5. 隐喻捕捉——对用户无意间抛出的比喻词高度敏感，立刻要求具象化。
     示例切入："你刚才说'这个地方感觉很脆'——在这个场景里，什么具体变量代表那个脆？"
五大评估维度（必须全部覆盖，顺序可灵活）：
  A. 情境判断——高度情境依赖、AI无法标准化的决策逻辑
  B. 跨域综合——跨领域整合经验，形成差异化视角
  C. 组织信任——关系资本、团队动员、利益方协调
  D. 反常识直觉——与行业主流相悖但被验证有效的判断
  E. 失败修正——失败后的复盘迭代与方法进化
深广平衡策略：
  • 每个维度在获得1-2条有效信息后，转向下一个尚未涉及的维度（广度优先）。
  • 五个维度全部触达后，对信息最丰富的维度发起二次深挖（深度跟进），并选用更高强度的提炼技术（优先 CIT 或 CTA）。
  • 若用户某条回答同时触及多个维度，记为均已覆盖，直接跳向空白维度。
  • 切换维度时用一句过渡语自然引导，不要突兀。
对话风格：像素描一样烘托轮廓——通过描绘决策环境、被排除的错误选项、依赖的微弱感知，把看不见的判断内核映衬出来。
信息边界：允许用户使用脱敏信息；避免主动索要真实公司名、真实客户名、手机号、身份证号、邮箱等敏感信息。
约束：每次只问一个问题；问题必须具体、可回答；不要打分，不要输出长总结；每条回复不超过120字。
Agent 工具协议：对话历史中可能出现以下标记——
  • 【tool:fetch_url】或【tool:read_document】：表示你已主动调用工具读取了外部资料；
  • 【tool_result】：紧随其后，是工具返回的结果摘要，资料全文已在系统 prompt 的外部资料节中。
当你在消息历史中看到这两条标记时，说明资料已完整加载，你必须立刻基于资料内容提出一个与当前阶段匹配的追问（先低门槛，再深问），推进访谈目标。禁止说"我来看看"、"稍等"等等待措辞。
资料邀请：若用户提到文章或报道但未附链接，自然邀请其粘贴 URL。`;
}

export function getFallbackQuestion(
  industry: ExpertIndustry,
  userTurnCount: number,
  fastTrack: boolean
): string {
  if (fastTrack) {
    const quick = [
      "你可以匿名回答。先说核心职责，我会据此给你定位价值杠杆。",
      "给一个代表性场景（不需真实公司名），我会提炼你的可复用方法。",
      "这个场景里你最关键的判断依据是什么？我会转成你的决策框架。",
      "很好，已具备最小信息。你可以继续补充，或直接生成分析报告。",
    ];
    return quick[Math.min(userTurnCount, quick.length - 1)];
  }

  const backgroundQuestions = [
    "先用一句话说你现在最核心的工作职责。",
    "你做这个方向多久了？主要服务哪类客户或业务？",
    "你最擅长解决哪 2-3 类问题？",
    "你日常最常用的方法或工具是什么（可含AI）？",
    "最近一个代表性项目是啥？一句话说背景和结果。",
  ];

  const deepQuestionsByIndustry: Record<ExpertIndustry, string[]> = {
    科技互联网: [
      // CIT: 规则失效场景
      "先不急着拆细节。就你经验看，技术方案最容易在什么情况下‘看起来可行、上线后翻车’？",
      // CTA: 微观决策拆解
      "如果用一个最近案例来说明，你当时主要在权衡哪两三个方向？最后为何选了现在这条？",
      // 新手-专家对比
      "如果让一个很聪明但刚入行的工程师接手你最复杂的那块系统，他最可能犯的毁灭性第一个错误是什么？",
      // 剥夺核心条件
      "假设今天完全没有监控数据和日志，你还能靠哪些旁证现象判断系统健康状态？",
    ],
    金融与投资: [
      // CIT: 规则失效场景
      "先从整体感觉说：你最常遇到哪类‘模型看起来很对，但你心里不踏实’的决策场景？",
      // CTA: 微观决策拆解
      "拿一个最近案例说说就好：你当时主要在收益、风险、时机之间怎么取舍？",
      // 新手-专家对比
      "一个聪明的新人拿到你常用的研究框架，最容易在哪一步自作聪明、反而踩进坑里？",
      // 剥夺核心条件
      "如果明天所有历史数据和模型都不可用，你还能靠哪些现场信号判断一笔交易的风险？",
    ],
    制造与供应链: [
      // CIT: 规则失效场景
      "先说大图景：在你这类交付里，最常见的‘表面都达标，但你仍不放心’是什么情况？",
      // CTA: 微观决策拆解
      "挑一个最近案例即可：你从发现异常到做判断，先看了哪些关键信号？",
      // 新手-专家对比
      "一个技术底子不差的新人接手你的核心供应链节点，最容易在哪里捅出大漏子？",
      // 剥夺核心条件
      "如果今天ERP系统全挂了，你只能靠现场观察，还能判断产能状态吗？你会看哪些物理信号？",
    ],
    教育与培训: [
      // CIT: 规则失效场景
      "先从经验判断说起：什么情况下课程数据看着不错，但你会怀疑学习效果并不扎实？",
      // CTA: 微观决策拆解
      "拿一个最近案例说说：你为什么判断这次该重构，而不是继续小修小补？",
      // 新手-专家对比
      "一个很有热情的新讲师拿到你的课程框架来讲，最容易在哪个环节翻车？",
      // 剥夺核心条件
      "如果完全没有数据和学员测评，你还能靠什么现场信号判断一门课效果是好是差？",
    ],
    医疗与健康: [
      // CIT: 规则失效场景
      "先说常见感受：在你工作里，什么情况下指标看着正常，但你会本能地提高警惕？",
      // CTA: 微观决策拆解
      "选一个最近场景就好：你在高压下做判断时，通常会先确认哪几个风险点？",
      // 新手-专家对比
      "一个临床基础扎实的新人独立处理你最复杂的那类病例或场景，最容易在哪里犯高代价的错误？",
      // 剥夺核心条件
      "如果你今天完全依赖不了检查报告，只能靠肉眼和对话观察，你会优先捕捉哪些信号来评估风险？",
    ],
    消费与零售: [
      // CIT: 规则失效场景
      "先说你常见的判断：什么情况下活动数据漂亮，但你会怀疑增长不可持续？",
      // CTA: 微观决策拆解
      "拿一个最近例子：你否掉某个增长方案时，主要是担心哪两三个后果？",
      // 新手-专家对比
      "一个数据分析能力很强的新人来做你的选品或运营决策，最容易被哪类数字迷惑、做出让你惋惜的判断？",
      // 剥夺核心条件
      "如果今天所有数据看板都不可用，你还能靠什么现场信号判断一个渠道或产品的真实健康度？",
    ],
    其他: [
      // CIT: 规则失效场景
      "先从整体经验说：什么情况下按惯例看没问题，但你会觉得‘这次不太一样’？",
      // CTA: 微观决策拆解
      "挑一个最近的困难决策：你当时主要在几个方案之间怎么权衡？",
      // 新手-专家对比
      "一个聪明但没经验的人拿到你的思路来执行，最容易在哪个环节自作聪明、反而踩坑？",
      // 剥夺核心条件
      "如果你平时依赖的最核心信息来源突然消失了，你还能靠哪些旁证信号做出可靠判断？",
    ],
  };

  if (userTurnCount < BACKGROUND_COLLECTION_MIN_TURNS) {
    const idx = Math.min(userTurnCount, backgroundQuestions.length - 1);
    return backgroundQuestions[idx];
  }

  const deepList = deepQuestionsByIndustry[industry];
  const deepIdx = Math.min(
    userTurnCount - BACKGROUND_COLLECTION_MIN_TURNS,
    deepList.length - 1
  );
  return deepList[deepIdx];
}

export function buildReportPrompt(
  messages: PromptChatMessage[],
  industry: ExpertIndustry,
  toolContext?: string
): string {
  const scoringAnchors = `评分量表（1-5整数，必须严格区分）：
1 = 刚入行，无体系，无法自主解决非标问题
2 = 基础操作，能执行常规任务，但无独特方法或框架
3 = 有方法但属行业共识，无显著差异化（大多数资深从业者在此区间）
4 = 有可验证的系统性突破，能解决他人难以解决的复杂问题，有案例支撑
5 = 极少数顶尖专家水平，行业标杆，方法被广泛借鉴

注意：给 4-5 分必须在对话或资料中找到明确证据；无证据的经验估算应保守给 2-3 分。`;

  const materialSection = toolContext
    ? `\n\n【外部参考资料（须在 uniqueSignals 和 actionPlan 中引用资料中的具体事实或观点）】\n${toolContext}`
    : "";

  return `你是专业价值分析师，专门评估哪些人类能力在 AI 时代仍具不可替代性。根据以下对话记录${toolContext ? "和外部参考资料" : ""}，输出严格 JSON 评估报告。

${scoringAnchors}

输出格式（仅输出 JSON，不含任何额外文字）：
{
  "answers": {
    "industryDepth": 1-5整数,
    "problemDefinition": 1-5整数,
    "crossDomainSynthesis": 1-5整数,
    "orgMomentum": 1-5整数,
    "aiCollaboration": 1-5整数,
    "methodologyTransferability": 1-5整数,
    "uniqueCase": "从对话或资料中提取的最具代表性的独特案例（50-120字）",
    "antiCase": "用户提到的失败修正或反常识判断（50-120字）",
    "next90DaysPlan": "基于用户现状给出的可执行90天建议（50-120字）"
  },
  "summary": "80-140字，点名用户的核心优势和当前最大卡点",
  "uniqueSignals": ["字符串", "字符串", "字符串"],
  "riskSignals": ["字符串", "字符串", "字符串"],
  "actionPlan": ["字符串", "字符串", "字符串"]
}

行业：${industry}。行业聚焦：${INDUSTRY_FOCUS[industry]}。

【对话记录】
${JSON.stringify(messages)}${materialSection}`;
}